package com.foozup.restaurant.model;

public class RestaurantBase {

	private Integer Id;
	private String Name;
	private String city;
	private String area;
	private String location;
	private String photo;
	private String cost;
	private String phoneNo;
	private String address;
	private String minDeliveryCost;
	
	 
       
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getPhoneNo() {
		return phoneNo;
	}
	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getMinDeliveryCost() {
		return minDeliveryCost;
	}
	public void setMinDeliveryCost(String minDeliveryCost) {
		this.minDeliveryCost = minDeliveryCost;
	}
	public Integer getId() {
		return Id;
	}
	public void setId(Integer id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
	

}
